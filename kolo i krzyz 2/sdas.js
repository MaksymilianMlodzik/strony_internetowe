function playerHasWon() {
    for (const condition of winningCombos) {
        let [a, b, c, d, e] = condition

        if(spaces[a] && (spaces[a] == spaces[b] && spaces[a] == spaces[c]) && spaces[a] == spaces[d] && spaces[a] == spaces[e]) {
            return [a,b,c,d,e]
        }
    }
    return false
}

